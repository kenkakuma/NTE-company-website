// 新文章模板生成器
export const createNewArticle = (data) => {
  const currentArticles = newsArticles;
  const nextId = Math.max(...currentArticles.map(a => a.id)) + 1;
  
  return {
    id: nextId,
    date: data.date || new Date().toISOString().slice(0, 10).replace(/-/g, '.'),
    title: data.title || '',
    category: data.category || '企業ニュース',
    excerpt: data.excerpt || '',
    content: data.content || '<p>文章内容...</p>',
    readTime: data.readTime || '3分',
    tags: data.tags || []
  };
};